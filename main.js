// storage keys

const STORAGE_KEY = 'scan_data';
const WELCOME_KEY = 'welcome_seen';

// variables

const MaxPlants = data.reduce((s, r) => s + r.plants.length, 0);
const SystemsCount = new Set(data.map(r => r.system)).size;
const saved = LoadData();

// document elements

const tbody = document.querySelector('#MatTable tbody');

// functions
function SaveData() {
    const state = {};
    document.querySelectorAll('.plant-check').forEach(checkbox => {
        if (checkbox.checked == true) {
            state[checkbox.id] = checkbox.checked;
        }
    });
    localStorage.setItem(STORAGE_KEY, JSON.stringify(state));
}

function LoadData() {
    try {
        return JSON.parse(localStorage.getItem(STORAGE_KEY) || '{}');
    } catch (error) {
        console.log(`Load data failed: ${error}`);
        return {};
    }
}

function UpdateEverything() {
    if (localStorage.getItem(WELCOME_KEY) === 'true') {
        document.getElementById('welcome-overlay').classList.add('hidden');
    }

    const checked = document.querySelectorAll('.plant-check:checked').length;
    const percentage = Math.round(checked / MaxPlants * 100);

    document.getElementById('progress-fill').style.width = percentage + '%';
    document.getElementById('progress-pct').textContent = percentage + '%';

    document.getElementById('stat-scanned').textContent = checked;
    document.getElementById('stat-remaining').textContent = MaxPlants - checked;

    document.getElementById('progress-label').textContent = checked + ' / ' + MaxPlants + ' scanned';

    document.getElementById('stat-systems').textContent = SystemsCount;
    document.getElementById('stat-plants').textContent = MaxPlants;
    
}

function UpdateSystemCheckbox(rowIdx) {
    const plants = document.querySelectorAll(`.row-plant[data-row="${rowIdx}"]`);
    const sysCb = document.getElementById(`sys-${rowIdx}`);
    if (!sysCb) return;
    
    sysCb.checked = [...plants].every(checkbox => checkbox.checked);
}

function TogglePlantCheckbox(checkbox, itemId, rowIdx) {
    const item = document.getElementById(itemId);
    if (!item) {
        console.error(`Item ${itemId} was not found in the document!`)
        return;
    }

    if (checkbox.checked) 
        item.classList.add('checked');
    else 
        item.classList.remove('checked');

    UpdateSystemCheckbox(rowIdx);
    UpdateEverything();
    SaveData();
}

function ToggleSystemCheckbox(SystemCheckbox, rowIdx) {
    document.querySelectorAll(`.row-plant[data-row="${rowIdx}"]`).forEach(PlantCheckbox => {
        PlantCheckbox.checked = SystemCheckbox.checked;
        const item = PlantCheckbox.closest('.plant-item');
        if (item) {
            if (SystemCheckbox.checked)
                item.classList.add('checked');
            else
                item.classList.remove('checked');
        }
    });
    UpdateEverything();
    SaveData();
}

function ProgressReset() {
    if (!confirm('Reset all scan progress? This cannot be undone.')) return;
    localStorage.removeItem(STORAGE_KEY);
    document.querySelectorAll('.plant-check').forEach(checkbox => {
        checkbox.checked = false;
        const item = checkbox.closest('.plant-item');
        if (item) item.classList.remove('checked');
    });
    UpdateEverything();
    SaveData();
}

function WelcomePopupClose() {
    if (document.getElementById('suppress-welcome').checked) {
        localStorage.setItem(WELCOME_KEY, 'true');
    }
    document.getElementById('welcome-overlay').classList.add('hidden');
}

